import { useEffect, useState } from "react";
import { Box, Text, useApp } from "ink";
import { mkdirSync, writeFileSync, existsSync } from "node:fs";
import { join } from "node:path";
import { Header, theme } from "../ui";

function parse(args: string[]): { name?: string; slot?: string; core?: string } {
  const o: any = {};
  for (let i = 0; i < args.length; i++) {
    const a = args[i];
    if (a.startsWith("--")) o[a.slice(2)] = args[++i] ?? "";
    else if (!o.name) o.name = a;
  }
  return o;
}

// Sanitize a project name into a valid C++ struct identifier (PascalCase-ish).
function toIdent(name: string): string {
  let s = name.replace(/[^A-Za-z0-9_]/g, "");
  if (!s || /^[0-9]/.test(s)) s = "C" + s;
  return s[0].toUpperCase() + s.slice(1);
}

function contractTemplate(): string {
  return `// Generated by \`qinit new\`. Minimal dynamic contract (qpi.h-constrained).
// Slot/name/core come from qinit.json. Build: \`qinit build\`  Deploy: \`qinit deploy --seed <55>\`
using namespace QPI;

struct CONTRACT_STATE2_TYPE
{
};

struct CONTRACT_STATE_TYPE : public ContractBase
{
    struct StateData
    {
        uint64 counter;
    };

    struct Inc_input {};
    struct Inc_output {};
    struct Get_input {};
    struct Get_output { uint64 value; };

    PUBLIC_PROCEDURE(Inc)
    {
        state.mut().counter += 1;
    }

    PUBLIC_FUNCTION(Get)
    {
        output.value = state.get().counter;
    }

    REGISTER_USER_FUNCTIONS_AND_PROCEDURES()
    {
        REGISTER_USER_PROCEDURE(Inc, 1);
        REGISTER_USER_FUNCTION(Get, 1);
    }

    INITIALIZE()
    {
        state.mut().counter = 0;
    }
};
`;
}

export function New({ args }: { args: string[] }) {
  const { exit } = useApp();
  const o = parse(args);
  const [log, setLog] = useState<string[]>([]);
  const [done, setDone] = useState(false);
  const add = (s: string) => setLog((l) => [...l, s]);

  useEffect(() => {
    try {
      if (!o.name) { add("usage: qinit new <name> [--core PATH]"); setDone(true); return; }
      const dir = o.name;
      const name = toIdent(o.name);
      const core = o.core ?? process.env.QINIT_CORE ?? "/home/kali/Projects/qubic-core-lite";
      if (existsSync(dir)) { add(`✗ '${dir}' already exists`); setDone(true); return; }

      mkdirSync(join(dir, "contracts"), { recursive: true });
      writeFileSync(join(dir, "contracts", `${name}.h`), contractTemplate());
      // No slot: the framework auto-allocates one at deploy by name (reuse-or-first-free).
      writeFileSync(join(dir, "qinit.json"), JSON.stringify(
        { name, contract: `contracts/${name}.h`, core, rpc: "http://127.0.0.1:41841" }, null, 2) + "\n");
      writeFileSync(join(dir, ".gitignore"), ["dist/", "*.so", "*.log", "qinit.idl.json", "contracts_dyn/", ".DS_Store"].join("\n") + "\n");
      writeFileSync(join(dir, "README.md"),
        `# ${name}\n\nQubic dynamic contract (scaffolded by qinit).\n\n` +
        "```bash\nqinit build                 # compile contracts/" + name + ".h -> .so + IDL\n" +
        "qinit deploy --seed <55-char-seed>   # build + upload + deploy (slot auto-allocated by name)\n" +
        "qinit call                  # interactive: pick contract -> fn/proc\n```\n\n" +
        "Config in `qinit.json` (name, contract, core, rpc). The deploy slot is chosen automatically.\n");

      add(`✓ created ${dir}/`);
      add(`  contracts/${name}.h`);
      add(`  qinit.json`);
      add(`  .gitignore, README.md`);
      add("");
      add(`next:  cd ${dir} && qinit build`);
      setDone(true);
    } catch (e: any) { add("ERROR: " + String(e?.message ?? e)); setDone(true); }
  }, []);
  useEffect(() => { if (done) exit(); }, [done]);

  const lineColor = (l: string) =>
    l.startsWith("✓") ? theme.ok : l.startsWith("✗") || l.startsWith("ERROR") ? theme.err : undefined;
  return (
    <Box flexDirection="column">
      <Header cmd="new" />
      {log.map((l, i) =>
        l.startsWith("next:")
          ? <Text key={i}><Text dimColor>next:</Text> <Text bold color={theme.accent}>{l.slice(5).trim()}</Text></Text>
          : <Text key={i} color={lineColor(l)} dimColor={l.startsWith("  ")}>{l}</Text>,
      )}
    </Box>
  );
}
