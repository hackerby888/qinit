// Bundle the test-SDK (src/sdk-entry.ts) into one self-contained string from the real @qinit source. Bun calls
// generateRuntimeMacro at Qinit bundle time and inlines the result, so there is no generated source artifact.
import { join } from "node:path";

const BUILD = join(import.meta.dir, "..");
const CORE_BROWSER = join(BUILD, "..", "core", "src", "browser.ts");

const HEADER = `// AUTO-GENERATED by \`qinit test\` — self-contained test SDK. Do not edit.\n`
  + `// Only external dep is the public @qubic-lib/qubic-ts-library (installed in your project),\n`
  + `// so \`bun test\` can run your tests against a deployed contract with no qinit monorepo.\n`
  + `// Bundled from the real @qinit/core + @qinit/proto source (packages/build/scripts/gen-runtime.ts).\n\n`;

// Build the bundled runtime text. Throws if the bundle fails or leaks a non-@qubic-lib external / node-only ref.
export async function generateRuntime(): Promise<string> {
  const r = await Bun.build({
    entrypoints: [join(BUILD, "src", "sdk-entry.ts")],
    target: "bun",
    minify: false,
    external: ["@qubic-lib/qubic-ts-library", "@qubic-lib"],
    plugins: [{ name: "core-browser", setup(b) { b.onResolve({ filter: /^@qinit\/core$/ }, () => ({ path: CORE_BROWSER })); } }],
  });
  if (!r.success) throw new Error("gen-runtime: bundle failed\n" + r.logs.map(String).join("\n"));
  const code = await r.outputs[0].text();

  const externals = [...new Set([...code.matchAll(/from\s*"([^"]+)"|require\("([^"]+)"\)/g)].map((m) => m[1] || m[2]).filter((x) => x && !x.startsWith(".") && !x.startsWith("/")))];
  const bad = externals.filter((x) => !x.startsWith("@qubic-lib"));
  if (bad.length || /\bnode:|child_process|require\("fs"\)/.test(code)) {
    throw new Error("gen-runtime: NON-PORTABLE bundle — unexpected externals: " + bad.join(", "));
  }
  return HEADER + code;
}

// Bun forbids a nested Bun.build in a macro worker because the outer bundler is waiting on that worker. Run the
// same generator in a short-lived Bun process instead, then inline its stdout as the macro result.
export function generateRuntimeMacro(): string {
  const result = Bun.spawnSync([process.execPath, import.meta.path]);
  if (result.exitCode !== 0) {
    throw new Error("gen-runtime macro failed\n" + new TextDecoder().decode(result.stderr));
  }
  return new TextDecoder().decode(result.stdout);
}

if (import.meta.main) {
  process.stdout.write(await generateRuntime());
}
