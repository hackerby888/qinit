// Generate a typed TS client from a contract IDL: named interfaces + a class whose methods
// wrap callFunction / invokeProcedure (no format strings for the caller). Flat inputs (scalars,
// id, m256i) get typed args; array/struct inputs fall back to a raw `inFmt: string` param.
import type { ContractIdl, Field } from "./idl";

// codec token -> TS type (QPI names).
const TS: Record<string, string> = {
  uint8: "number", uint16: "number", uint32: "number",
  sint8: "number", sint16: "number", sint32: "number", bit: "number",
  uint64: "bigint", sint64: "bigint",
  id: "string", m256i: "string",
};
function tsType(tok: string): string {
  if (tok.startsWith("[")) return `${tsType(tok.slice(tok.indexOf(";") + 1, tok.lastIndexOf("]")).trim())}[]`;
  if (tok.startsWith("{")) return "unknown"; // nested struct (v1: loose)
  return TS[tok] ?? "unknown";
}
const flat = (fs: Field[]) => fs.every((f) => !f.type.startsWith("[") && !f.type.startsWith("{"));

function iface(name: string, fields: Field[]): string {
  if (!fields.length) return `export interface ${name} {}`;
  return `export interface ${name} {\n${fields.map((f) => `  ${f.name}: ${tsType(f.type)};`).join("\n")}\n}`;
}
function outMap(fields: Field[]): string {
  if (!fields.length) return "return {};";
  if (fields.length === 1) return `return { ${fields[0].name}: r as ${tsType(fields[0].type)} };`;
  return `const a = r as unknown[]; return { ${fields.map((f, i) => `${f.name}: a[${i}] as ${tsType(f.type)}`).join(", ")} };`;
}
// value-format template literal from a typed `args` object (flat fields only).
const inTmpl = (fs: Field[]) => fs.length ? "`" + fs.map((f) => `\${args.${f.name}}${f.type}`).join(", ") + "`" : '""';

// runtimeImport: when set (e.g. "./runtime"), pull LiteRpc/callFunction/invokeProcedure from one
// self-contained module instead of @qinit/* — used by `qinit test`'s emitted SDK.
export function generateClient(idl: ContractIdl, index: number, opts?: { runtimeImport?: string }): string {
  const L: string[] = [];
  L.push(`// AUTO-GENERATED by \`qinit gen\` — typed client for ${idl.name} (slot ${index}). Do not edit.`);
  if (opts?.runtimeImport) {
    L.push(`import { LiteRpc, callFunction, invokeProcedure } from "${opts.runtimeImport}";`);
  } else {
    L.push(`import { LiteRpc } from "@qinit/core";`);
    L.push(`import { callFunction, invokeProcedure } from "@qinit/proto";`);
  }
  L.push("");
  for (const e of Object.values(idl.functions)) {
    L.push(iface(`${e.name}_input`, e.inFields));
    L.push(iface(`${e.name}_output`, e.outFields ?? []));
  }
  for (const e of Object.values(idl.procedures)) L.push(iface(`${e.name}_input`, e.inFields));
  L.push("");
  L.push(`export interface ${idl.name}Opts { rpc?: LiteRpc; rpcBase?: string; index?: number; seed?: string }`);
  L.push("");
  L.push(`export class ${idl.name} {`);
  L.push(`  rpc: LiteRpc; rpcBase: string; index: number; seed?: string;`);
  L.push(`  constructor(o: ${idl.name}Opts = {}) {`);
  L.push(`    this.rpcBase = o.rpcBase ?? "http://127.0.0.1:41841";`);
  L.push(`    this.rpc = o.rpc ?? new LiteRpc(this.rpcBase);`);
  L.push(`    this.index = o.index ?? ${index};`);
  L.push(`    this.seed = o.seed;`);
  L.push(`  }`);
  for (const [id, e] of Object.entries(idl.functions)) {
    const out = e.outFields ?? [];
    const inFlat = flat(e.inFields);
    const param = e.inFields.length ? (inFlat ? `args: ${e.name}_input` : "inFmt: string") : "";
    const inFmt = !e.inFields.length ? '""' : inFlat ? inTmpl(e.inFields) : "inFmt";
    L.push("");
    L.push(`  /** read-only */`);
    L.push(`  async ${e.name}(${param}): Promise<${e.name}_output> {`);
    L.push(`    const r = await callFunction(this.rpc, this.index, ${id}, ${inFmt}, ${JSON.stringify(out.map((f) => f.type).join(", "))});`);
    L.push(`    ${outMap(out)}`);
    L.push(`  }`);
  }
  for (const [id, e] of Object.entries(idl.procedures)) {
    const inFlat = flat(e.inFields);
    const param = e.inFields.length ? (inFlat ? `args: ${e.name}_input, ` : "inFmt: string, ") : "";
    const inFmt = !e.inFields.length ? '""' : inFlat ? inTmpl(e.inFields) : "inFmt";
    L.push("");
    L.push(`  /** transaction — auto-confirms (resolves once processed) unless { confirm: false } */`);
    L.push(`  async ${e.name}(${param}opts: { seed?: string; amount?: number; confirm?: boolean } = {}): Promise<{ ok: boolean; txId?: string; tick?: number; confirmed?: boolean; included?: boolean; moneyFlew?: boolean }> {`);
    L.push(`    const ti = (await this.rpc.tickInfo()) as { tick?: number };`);
    L.push(`    const seed = opts.seed ?? this.seed ?? (await this.rpc.fundedSeed()) ?? "a".repeat(55);`);
    L.push(`    const r = await invokeProcedure({ seed, rpcBase: this.rpcBase, contractIndex: this.index, procId: ${id}, amount: opts.amount ?? 0, inFmt: ${inFmt}, tick: (ti.tick ?? 0) + 8, confirm: opts.confirm !== false, rpc: this.rpc });`);
    L.push(`    return { ok: r.ok, txId: r.txId, tick: r.tick, confirmed: r.confirmed, included: r.included, moneyFlew: r.moneyFlew };`);
    L.push(`  }`);
  }
  L.push(`}`);
  return L.join("\n") + "\n";
}
