// Generate a typed TS client from a contract IDL: named interfaces + a class whose methods
// wrap callFunction / invokeProcedure (no format strings for the caller). Flat inputs stay deterministic and easy to fuzz.
import type { ContractIdl, Field } from "./idl";

// codec token -> TS type (QPI names).
const TS: Record<string, string> = {
  uint8: "number",
  uint16: "number",
  uint32: "number",
  sint8: "number",
  sint16: "number",
  sint32: "number",
  bit: "number",
  uint64: "bigint",
  sint64: "bigint",
  id: "string",
  m256i: "string",
};
function tsType(tok: string): string {
  if (tok.startsWith("[")) {
    return `${tsType(tok.slice(tok.indexOf(";") + 1, tok.lastIndexOf("]")).trim())}[]`;
  }
  if (tok.startsWith("{")) return "unknown"; // unresolved nested struct (no field tree) — best effort
  return TS[tok] ?? "unknown";
}
// TS type of a field, using its resolved nested struct/array tree when present (inline object types).
function tsTypeOf(f: Field): string {
  if (f.struct) {
    const obj = `{ ${f.struct.map((c) => `${c.name}: ${tsTypeOf(c)}`).join("; ")} }`;
    return f.array ? `${obj}[]` : obj;
  }
  return tsType(f.type);
}

// A field is value-template encodable (typed `args`) if it's a scalar/id or a nested struct of such — but NOT an
// array (a fixed-N Array can't be sized from a value template), which keeps the raw `inFmt` fallback.
function flatField(f: Field): boolean {
  if (f.array) return false;
  if (f.struct) return f.struct.every(flatField);
  return true;
}
const flat = (fs: Field[]) => fs.every(flatField);

function iface(name: string, fields: Field[]): string {
  if (!fields.length) return `export interface ${name} {}`;
  return `export interface ${name} {\n${fields.map((f) => `  ${f.name}: ${tsTypeOf(f)};`).join("\n")}\n}`;
}

// Map the decoder's positional value (structs decode to ordered arrays) at `acc` into the named/nested shape.
function mapExpr(f: Field, acc: string): string {
  if (f.struct) {
    const obj = (element: string) =>
      `{ ${f.struct!
        .map((child, index) => `${child.name}: ${mapExpr(child, `${element}[${index}]`)}`)
        .join(", ")} }`;
    if (f.array) return `(${acc} as unknown[][]).map((e) => (${obj("e")}))`;
    return `((s) => (${obj("s")}))(${acc} as unknown[])`;
  }
  return `${acc} as ${tsTypeOf(f)}`;
}
function outMap(fields: Field[]): string {
  if (!fields.length) return "return {};";
  if (fields.length === 1) return `return { ${fields[0].name}: ${mapExpr(fields[0], "r")} };`;
  return `const a = r as unknown[]; return { ${fields
    .map((field, index) => `${field.name}: ${mapExpr(field, `a[${index}]`)}`)
    .join(", ")} };`;
}

// value-format template literal from a typed `args` object — recurses into nested structs as `{ ... }` groups
// (the encoder gives a braced group its own alignment, matching the C++ struct layout). Flat-fields only.
function inTmplFields(fs: Field[], prefix: string): string {
  return fs
    .map((field) =>
      field.struct
        ? `{ ${inTmplFields(field.struct, `${prefix}.${field.name}`)} }`
        : `\${${prefix}.${field.name}}${field.type}`,
    )
    .join(", ");
}
const inTmpl = (fs: Field[]) => (fs.length ? "`" + inTmplFields(fs, "args") + "`" : '""');

// runtimeImport: when set (e.g. "./runtime"), pull LiteRpc/callFunction/invokeProcedure from one
// self-contained module instead of @qinit/* — used by `qinit test`'s emitted SDK.
export function generateClient(
  idl: ContractIdl,
  index: number,
  opts?: { runtimeImport?: string },
): string {
  const lines: string[] = [];
  lines.push(`// AUTO-GENERATED by \`qinit gen\` — typed client for ${idl.name} (slot ${index}). Do not edit.`);
  if (opts?.runtimeImport) {
    lines.push(`import { LiteRpc, callFunction, invokeProcedure } from "${opts.runtimeImport}";`);
  } else {
    lines.push(`import { LiteRpc } from "@qinit/core";`);
    lines.push(`import { callFunction, invokeProcedure } from "@qinit/proto";`);
  }
  lines.push("");
  for (const e of Object.values(idl.functions)) {
    lines.push(iface(`${e.name}_input`, e.inFields));
    lines.push(iface(`${e.name}_output`, e.outFields ?? []));
  }
  for (const e of Object.values(idl.procedures)) lines.push(iface(`${e.name}_input`, e.inFields));
  lines.push("");
  lines.push(`export interface ${idl.name}Opts { rpc?: LiteRpc; rpcBase?: string; index?: number; seed?: string }`);
  lines.push("");
  lines.push(`export class ${idl.name} {`);
  lines.push(`  rpc: LiteRpc; rpcBase: string; index: number; seed?: string;`);
  lines.push(`  constructor(o: ${idl.name}Opts = {}) {`);
  lines.push(`    this.rpcBase = o.rpcBase ?? "http://127.0.0.1:41841";`);
  lines.push(`    this.rpc = o.rpc ?? new LiteRpc(this.rpcBase);`);
  lines.push(`    this.index = o.index ?? ${index};`);
  lines.push(`    this.seed = o.seed;`);
  lines.push(`  }`);
  for (const [id, e] of Object.entries(idl.functions)) {
    const out = e.outFields ?? [];
    const inFlat = flat(e.inFields);
    const param = e.inFields.length ? (inFlat ? `args: ${e.name}_input` : "inFmt: string") : "";
    const inFmt = !e.inFields.length ? '""' : inFlat ? inTmpl(e.inFields) : "inFmt";
    lines.push("");
    lines.push(`  /** read-only */`);
    lines.push(`  async ${e.name}(${param}): Promise<${e.name}_output> {`);
    lines.push(`    const r = await callFunction(this.rpc, this.index, ${id}, ${inFmt}, ${JSON.stringify(out.map((f) => f.type).join(", "))});`);
    lines.push(`    ${outMap(out)}`);
    lines.push(`  }`);
  }
  for (const [id, e] of Object.entries(idl.procedures)) {
    const inFlat = flat(e.inFields);
    const param = e.inFields.length ? (inFlat ? `args: ${e.name}_input, ` : "inFmt: string, ") : "";
    const inFmt = !e.inFields.length ? '""' : inFlat ? inTmpl(e.inFields) : "inFmt";
    lines.push("");
    lines.push(`  /** transaction — auto-confirms (resolves once processed) unless { confirm: false } */`);
    lines.push(`  async ${e.name}(${param}opts: { seed?: string; amount?: number; confirm?: boolean } = {}): Promise<{ ok: boolean; txId?: string; tick?: number; confirmed?: boolean; included?: boolean; moneyFlew?: boolean }> {`);
    lines.push(`    const ti = (await this.rpc.tickInfo()) as { tick?: number };`);
    lines.push(`    const seed = opts.seed ?? this.seed ?? (await this.rpc.fundedSeed()) ?? "a".repeat(55);`);
    lines.push(`    const r = await invokeProcedure({ seed, rpcBase: this.rpcBase, contractIndex: this.index, procId: ${id}, amount: opts.amount ?? 0, inFmt: ${inFmt}, tick: (ti.tick ?? 0) + 8, confirm: opts.confirm !== false, rpc: this.rpc });`);
    lines.push(`    return { ok: r.ok, txId: r.txId, tick: r.tick, confirmed: r.confirmed, included: r.included, moneyFlew: r.moneyFlew };`);
    lines.push(`  }`);
  }
  lines.push(`}`);
  return lines.join("\n") + "\n";
}
