// Compile a qpi.h-constrained contract .h into a dynamic-contract .so.
// Pinned recipe from DYNAMIC_CONTRACTS.md §5.4: include qpi.h + lite_dyn_abi.h,
// never contract_exec.h; define LITE_DYN_SO_BUILD + CONTRACT_INDEX/STATE_TYPE.
import { mkdir, writeFile } from "node:fs/promises";
import { join } from "node:path";

export interface BuildOpts {
  contractPath: string; // absolute path to the contract .h
  name: string;         // CONTRACT_STATE_TYPE (struct name)
  slot: number;         // CONTRACT_INDEX
  corePath: string;     // qubic-core-lite root
  outDir: string;
  clang?: string;       // default clang++-18
  calleePrelude?: string; // inter-contract: callee type headers + inputType consts (from intercontract.ts)
  dynCallees?: Record<string, { header: string; index: number }>; // dynamic (Qinit-deployed) callees
  toolchain?: "native" | "wasm" | "auto"; // compiler backend; auto = wasm if cached else native
  platform?: string;    // target node platform for the wasm toolchain (linux-x64 | linux-arm64 | darwin-arm64)
  target?: "so" | "wasm"; // OUTPUT format: native .so/.dylib (default) or a wasm contract (run by the node's WAMR engine)
  wasmClang?: string;   // clang targeting wasm32-wasi (for target=wasm); default env WASM_CLANG / clang++
  wasmSysroot?: string; // wasi-sysroot with libc++ headers; default env WASI_SYSROOT
}

export function genWrapper(o: BuildOpts): string {
  return `// generated by qinit build — do not edit
#define NO_UEFI   // OS process, not UEFI: platform/memory.h uses std mem ops, not the UEFI 'bs' global
#include <cstdint>
#include <cstddef>
#include <cstring>
#include <cstdlib>
#include <string>
#include <type_traits>
#include <utility>
#include <array>
#include <limits>
#define LITE_DYN_SO_BUILD
#include "contract_core/pre_qpi_def.h"
#include "contracts/qpi.h"
#include "contract_core/qpi_proposal_voting.h"
#include "oracle_core/oracle_interfaces_def.h"
${o.calleePrelude ?? ""}
#define CONTRACT_INDEX ${o.slot}
#define ${o.name}_CONTRACT_INDEX ${o.slot}
#define CONTRACT_STATE_TYPE ${o.name}
#define CONTRACT_STATE2_TYPE ${o.name}2
#include "${o.contractPath}"
// QPI data-structure impls operate on contract-local memory -> .so-safe. CAUTION: after the contract.
// Collection + LinkedList are clean (only qpi.h + memory).
#include "contract_core/qpi_collection_impl.h"
#include "contract_core/qpi_linked_list_impl.h"
// HashMap's impl pulls common_buffers.h, whose __acquire/releaseScratchpad use the host-initialized
// commonBuffers global (absent in the .so). Rename those defs to dead symbols; the .so forwards
// scratchpad to the host via lite_dyn_abi instead.
#define __acquireScratchpad __lite_cb_acquireScratchpad_unused
#define __releaseScratchpad __lite_cb_releaseScratchpad_unused
#include "contract_core/qpi_hash_map_impl.h"
#undef __acquireScratchpad
#undef __releaseScratchpad
#include "extensions/lite_dyn_abi.h"
`;
}

// --- wasm contract target (contract compiled TO wasm, run by the node's WAMR engine) ---
// Same TU as the .so, but binds to lite_wasm_tu.h (qpi.h calls -> "lhost" wasm imports + dispatch/reg/state
// exports) instead of lite_dyn_abi.h. See core src/extensions/WASM_CONTRACTS.md §13.11.
export function genWrapperWasm(o: BuildOpts): string {
  return genWrapper(o)
    .replace("#define LITE_DYN_SO_BUILD", "#define LITE_WASM_TU_BUILD")
    .replace('#include "extensions/lite_dyn_abi.h"', '#include "extensions/lite_wasm_tu.h"');
}

export interface WasmCompileResult {
  ok: boolean;
  wasm: string;
  wrapper: string;
  stderr: string;
  exitCode: number | null;
}

// clang must target wasm32-wasi (the bundled clang.wasm multitool has the WebAssembly backend; a native
// wasi-sdk clang++ also works). wasmSysroot = the wasi-sysroot with libc++ headers.
export async function compileWasmContract(
  o: BuildOpts & { wasmClang?: string; wasmSysroot?: string },
): Promise<WasmCompileResult> {
  const src = join(o.corePath, "src");
  await mkdir(o.outDir, { recursive: true });
  const wrapper = join(o.outDir, `${o.name}.wasm.wrapper.cpp`);
  await writeFile(wrapper, genWrapperWasm(o));
  const wasm = join(o.outDir, `${o.name}.wasm`);
  const clang = o.wasmClang ?? process.env.WASM_CLANG ?? "clang++";
  const sysroot = o.wasmSysroot ?? process.env.WASI_SYSROOT;
  const shim = join(src, "extensions", "lite_wasm_intrinsics.h"); // wasm32 shims for the x86 platform intrinsics
  // -DLITEDYN_CONTRACT_TU gates the node-only throw in utils.h; -fno-exceptions/-fno-rtti keep the module lean;
  // reactor + --no-entry => a library wasm (no _start); --allow-undefined leaves the lhost imports unresolved
  // (the node binds them). No -mavx2 (wasm); m256i is emulated via SIMDe.
  const args = [
    "--target=wasm32-wasi", "-std=c++20", "-O2", "-fno-rtti", "-fno-exceptions",
    "-DLITEDYN_CONTRACT_TU", "-include", shim,
    ...(sysroot ? [`--sysroot=${sysroot}`] : []),
    `-I${o.corePath}`, `-I${src}`,
    "-Wl,--no-entry", "-Wl,--allow-undefined", "-mexec-model=reactor",
    wrapper, "-o", wasm,
  ];
  const p = Bun.spawn([clang, ...args], { stdout: "pipe", stderr: "pipe" });
  const stderr = await new Response(p.stderr).text();
  await p.exited;
  return { ok: p.exitCode === 0, wasm, wrapper, stderr, exitCode: p.exitCode };
}

// Any clang >= 18 (clang only — relies on its intrinsics/ABI). Not gcc.
function clangMajor(bin: string): number | null {
  try {
    const r = Bun.spawnSync([bin, "--version"]);
    if (r.exitCode !== 0) return null;
    const m = new TextDecoder().decode(r.stdout).match(/clang version (\d+)/);
    return m ? Number(m[1]) : null;
  } catch { return null; }
}

export function resolveClang(pref?: string): string {
  const cands = [pref, "clang++-18", "clang++-19", "clang++-20", "clang++"].filter(Boolean) as string[];
  for (const c of cands) {
    const v = clangMajor(c);
    if (v !== null && v >= 18) return c;
  }
  throw new Error("no clang++ >= 18 found on PATH (clang required, not gcc)");
}

export interface CompileResult {
  ok: boolean;
  so: string;
  wrapper: string;
  stderr: string;
  exitCode: number | null;
}

export async function compile(o: BuildOpts): Promise<CompileResult> {
  const src = join(o.corePath, "src");
  await mkdir(o.outDir, { recursive: true });
  const wrapper = join(o.outDir, `${o.name}.wrapper.cpp`);
  await writeFile(wrapper, genWrapper(o));
  const so = join(o.outDir, `${o.name}.so`);
  const clang = resolveClang(o.clang);
  // -mavx2 is mandatory: m256i uses AVX intrinsics and the host compiles with AVX2,
  // so the .so must match for the m256i pass-by-value ABI.
  // -fno-exceptions + LITEDYN_CONTRACT_TU make the .so reference only cross-ABI-standard symbols
  // (operator new/delete, libc mem*/malloc) that every node already provides, regardless of platform
  // or STL (libstdc++ vs libc++) — so it needs no per-platform sysroot and loads anywhere. The macro
  // gates the one node-only throw in extensions/utils.h; the node build (macro undefined) is unchanged.
  const args = ["-std=c++20", "-O2", "-fPIC", "-shared", "-fno-rtti", "-fno-exceptions", "-DLITEDYN_CONTRACT_TU", "-mavx2",
    `-I${o.corePath}`, `-I${src}`, wrapper, "-o", so];
  const p = Bun.spawn([clang, ...args], { stdout: "pipe", stderr: "pipe" });
  const stderr = await new Response(p.stderr).text();
  await p.exited;
  return { ok: p.exitCode === 0, so, wrapper, stderr, exitCode: p.exitCode };
}
