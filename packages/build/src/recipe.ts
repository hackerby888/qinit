// Compile a qpi.h-constrained contract .h into a wasm contract module (run by the node's WAMR engine).
import { mkdir, writeFile, copyFile } from "node:fs/promises";
import { mkdirSync, existsSync, writeFileSync } from "node:fs";
import { join } from "node:path";
import { tmpdir } from "node:os";
import { createHash } from "node:crypto";
import { wasiSdkPaths } from "@qinit/core/project";
import { CORE_WASM_HEADERS } from "@qinit/core/wasm-headers";
import { writeLineMap } from "./linemap";
import WASM_GTEST_H from "./assets/wasm_gtest.h" with { type: "text" };
import WASM_CONTRACT_TESTING_H_TEMPLATE from "./assets/wasm_contract_testing.h" with { type: "text" };
import TEST_UTIL_H from "./assets/test_util.h" with { type: "text" };

const WASM_CONTRACT_TESTING_H = WASM_CONTRACT_TESTING_H_TEMPLATE.replace(
  "__QINIT_CORE_WASM_ABI_METADATA__",
  CORE_WASM_HEADERS.shared.abiMetadata,
);

export const WASM_CONTRACT_TESTING_HEADER = WASM_CONTRACT_TESTING_H;
export const WASM_TEST_UTIL_HEADER = TEST_UTIL_H;

export interface BuildOpts {
  contractPath: string; // absolute path to the contract .h
  name: string;         // contract name (artifact filenames + IDL); also the C++ struct type unless stateType is set
  stateType?: string;   // the C++ contract struct type for the wrapper #defines — differs from `name` when the
                        // on-chain ticker isn't the struct name (e.g. system contract QTRY -> struct QUOTTERY)
  slot: number;         // CONTRACT_INDEX
  corePath: string;     // qubic-core-lite root
  outDir: string;
  calleePrelude?: string; // inter-contract: callee type headers + inputType consts (from intercontract.ts)
  dynCallees?: Record<string, { header: string; index: number }>; // dynamic (Qinit-deployed) callees
  wasmClang?: string;   // clang targeting wasm32-wasi; default env WASM_CLANG / the auto-fetched wasi-sdk
  wasmSysroot?: string; // wasi-sysroot with libc++ headers; default env WASI_SYSROOT / the auto-fetched wasi-sdk
  skipVerify?: boolean; // skip the qpi.h protocol gate (compile-only; the upstream verifier can't parse some Wasm macros)
  arenaSz?: number;     // contract-side arena size (WASM_ARENA_SIZE), default 1GB; shrink for browser IDE builds
  testSource?: string;  // core-lite contract_testing.h-style source compiled into a private Wasm runner
  testPath?: string;    // display path for the test source (a #line directive maps EXPECT_* file:line back to it)
  extraCompileFlags?: string[]; // appended to the consuming compile only (not the PCH); used by the gtest
                        // corpus path to relax test-harness-only diagnostics (e.g. -Wno-error=return-mismatch)
  sharedMemBase?: number; // shared-memory gtest mode: link with --import-memory + --global-base=<this>, so the
                        // module lives inside the corpus runner's memory at the given byte offset. The engine
                        // instantiates it with the runner's Memory and the runner's contractStates[i] pointer
}

// The stable preamble shared by every contract build — std headers, then the big QPI/core headers. It has no
// per-contract macros (those come after), so it's identical across contracts and can be precompiled (PCH).
const PREAMBLE_STD = `// generated by qinit build — do not edit
#define NO_UEFI   // OS process, not UEFI: platform/memory.h uses std mem ops, not the UEFI 'bs' global
#include <cstdint>
#include <cstddef>
#include <cstring>
#include <cstdlib>
#include <string>
#include <type_traits>
#include <utility>
#include <array>
#include <limits>
`;
const PREAMBLE_CORE = `#include "contract_core/pre_qpi_def.h"
#include "contracts/qpi.h"
#include "contracts/math_lib.h"
#include "contract_core/qpi_proposal_voting.h"
#include "oracle_core/oracle_interfaces_def.h"
`;

// Exactly what the Wasm wrapper emits before any per-contract content, so a PCH built from this is a valid
// prefix of the wrapper TU.
export function buildPreamble(): string {
  return `${PREAMBLE_STD}#define LITE_WASM_TU_BUILD\n${PREAMBLE_CORE}`;
}

// --- wasm contract target (contract compiled TO wasm, run by the node's WAMR engine) ---
export function genWrapperWasm(o: BuildOpts): string {
  const T = o.stateType ?? o.name; // the C++ struct type the contract declares
  const wrapper = `${buildPreamble()}${o.calleePrelude ?? ""}
#define CONTRACT_INDEX ${o.slot}
#define ${T}_CONTRACT_INDEX ${o.slot}
#define CONTRACT_STATE_TYPE ${T}
#define CONTRACT_STATE2_TYPE ${T}2
// Late-bind CALL/INVOKE_OTHER_CONTRACT to the callee's deployed code via the host (needs the callee
// __contract_index + <Type>_<fn>_inputType consts from the prelude + CONTRACT_INDEX above).
#include "${CORE_WASM_HEADERS.sdk.intercontractCalls}"
#include "${CORE_WASM_HEADERS.sdk.qpiSupport}"
#include "${o.contractPath}"
// QPI data-structure impls operate on contract-local memory. CAUTION: after the contract.
// Collection + LinkedList are clean (only qpi.h + memory).
#include "contract_core/qpi_collection_impl.h"
#include "contract_core/qpi_linked_list_impl.h"
// HashMap's impl pulls common_buffers.h, whose __acquire/releaseScratchpad use the host-initialized
// commonBuffers global (absent in the Wasm module). Rename those defs to dead symbols; calls use host imports.
#define __acquireScratchpad __qinit_cb_acquireScratchpad_unused
#define __releaseScratchpad __qinit_cb_releaseScratchpad_unused
#include "contract_core/qpi_hash_map_impl.h"
#undef __acquireScratchpad
#undef __releaseScratchpad
#include "${CORE_WASM_HEADERS.sdk.moduleRuntime}"
`;
  if (!o.testSource) {
    return wrapper;
  }
  // Test runner module: inject Qinit's private TEST/EXPECT registry before the standard core-lite-style
  // source. The source itself uses contract_testing.h / ContractTesting; only the runner implementation is
  const testPath = (o.testPath ?? `${o.name}.test.cpp`).replace(/\\/g, "/").replace(/"/g, "");
  return `${wrapper}#define QINIT_WASM_GTEST
${WASM_GTEST_H}
#line 1 "${testPath}"
${o.testSource}
`;
}

export interface WasmCompileResult {
  ok: boolean;
  wasm: string;
  wrapper: string;
  stderr: string;
  exitCode: number | null;
  debugWasm?: string;   // -g DWARF sidecar; the deployed wasm is stripped
  linesJson?: string;   // build-time {fileOffset -> file:line:func} map for trap backtraces (100% at -O0)
}

// --- precompiled header (PCH) of the stable preamble ----------------------------------------------------
// One clang on one TU is single-threaded; the slow part is re-parsing qpi.h + the core headers on EVERY
let pchState: string | null | undefined;
let pchInflight: Promise<string | null> | null = null;

function disablePch(): void {
  pchState = null;
}

// Build (or reuse) a PCH of the wasm preamble with exactly `pchFlags` (the compile flags incl. -include shim).
// Cached on disk keyed by clang + flags + preamble, so it survives across builds. Never throws — null means
async function ensureWasmPch(clang: string, pchFlags: string[]): Promise<string | null> {
  if (process.env.QINIT_NO_PCH) {
    return null; // operational escape hatch — compile without a precompiled header
  }
  if (pchState !== undefined) {
    return pchState;
  }
  if (pchInflight) {
    return pchInflight;
  }

  pchInflight = (async () => {
    try {
      const preamble = buildPreamble();
      const sig = createHash("sha256").update(clang).update("\0").update(pchFlags.join(" ")).update("\0").update(preamble).digest("hex").slice(0, 24);
      const dir = join(tmpdir(), "qinit-pch");
      mkdirSync(dir, { recursive: true });
      const pch = join(dir, `wasm-${sig}.pch`);
      if (existsSync(pch)) {
        pchState = pch;
        return pch;
      }

      const hpp = join(dir, `wasm-${sig}.hpp`);
      writeFileSync(hpp, preamble);
      const p = Bun.spawn([clang, ...pchFlags, "-x", "c++-header", hpp, "-o", pch], { stdout: "pipe", stderr: "pipe" });
      await new Response(p.stderr).text();
      await p.exited;
      pchState = p.exitCode === 0 && existsSync(pch) ? pch : null;
      return pchState;
    } catch {
      pchState = null;
      return null;
    } finally {
      pchInflight = null;
    }
  })();

  return pchInflight;
}

// clang must target wasm32-wasi (the bundled clang.wasm multitool has the WebAssembly backend; a native
// wasi-sdk clang++ also works). wasmSysroot = the wasi-sysroot with libc++ headers.
export async function compileWasmContract(
  o: BuildOpts & { wasmClang?: string; wasmSysroot?: string },
): Promise<WasmCompileResult> {
  const src = join(o.corePath, "src");
  await mkdir(o.outDir, { recursive: true });
  const wrapper = join(o.outDir, `${o.name}.wasm.wrapper.cpp`);
  await writeFile(wrapper, genWrapperWasm(o));
  const wasm = join(o.outDir, `${o.name}.wasm`);
  const sdk = wasiSdkPaths();   // auto-fetched by `qinit node run`; cached under ~/.cache/qinit/wasi-sdk
  const clang = o.wasmClang ?? process.env.WASM_CLANG ?? sdk?.clang ?? "clang++";
  const sysroot = o.wasmSysroot ?? process.env.WASI_SYSROOT ?? sdk?.sysroot;
  const shim = join(src, CORE_WASM_HEADERS.sdk.platformIntrinsics);
  // -DLITEDYN_CONTRACT_TU gates the node-only throw in utils.h; -fno-exceptions/-fno-rtti keep the module lean;
  // reactor + --no-entry => a library wasm (no _start); --allow-undefined leaves the lhost imports unresolved
  const compileFlags = [
    "--target=wasm32-wasi", "-std=c++20", "-O0", "-g", "-fno-rtti", "-fno-exceptions",
    "-DLITEDYN_CONTRACT_TU",
    ...(o.arenaSz ? [`-DWASM_ARENA_SIZE=${o.arenaSz}`] : []),
    ...(sysroot ? [`--sysroot=${sysroot}`] : []),
    `-I${o.corePath}`, `-I${src}`,
  ];
  const shimFlag = ["-include", shim];
  const linkFlags = ["-Wl,--no-entry", "-Wl,--allow-undefined", "-mexec-model=reactor"];
  if (o.sharedMemBase !== undefined) {
    // Shared-memory gtest mode: relocate the module's whole static footprint (data, stack, state buffer,
    // arena) above the given base and import the memory instead of owning one. The shadow stack rides just
    linkFlags.push("-Wl,--import-memory", `-Wl,--global-base=${o.sharedMemBase >>> 0}`, "-Wl,-z,stack-size=8388608");
  }

  // Reuse the precompiled preamble when available. The PCH already carries -include shim (whose static-inline
  // defs aren't include-guarded), so the PCH-consuming compile must NOT repeat it; the no-PCH path keeps it.
  const pch = await ensureWasmPch(clang, [...compileFlags, ...shimFlag]);

  const runClang = async (front: string[], withShim: boolean) => {
    const args = [...front, ...compileFlags, ...(withShim ? shimFlag : []), ...(o.extraCompileFlags ?? []), ...linkFlags, wrapper, "-o", wasm];
    const cp = Bun.spawn([clang, ...args], { stdout: "pipe", stderr: "pipe" });
    const out = await new Response(cp.stderr).text();
    await cp.exited;
    return { exitCode: cp.exitCode, stderr: out };
  };

  let { exitCode, stderr } = pch ? await runClang(["-include-pch", pch], false) : await runClang([], true);

  // A stale/incompatible PCH surfaces as an AST/PCH error (not a contract error) — disable it and retry once
  // without, so a bad PCH never fails a valid contract.
  if (exitCode !== 0 && pch && /\b(PCH|AST file|precompiled header|module file)\b/i.test(stderr)) {
    disablePch();
    ({ exitCode, stderr } = await runClang([], true));
  }

  // -g leaves DWARF in `wasm`: copy it as the debug sidecar (qinit symbolizes trap offsets against it), then
  // strip DWARF from the deployed wasm in place -> code byte-identical, so offsets still match the sidecar.
  let debugWasm: string | undefined, linesJson: string | undefined;
  if (exitCode === 0) {
    try {
      // clang's directory — handle BOTH separators. On Windows WASM_CLANG is a backslash path
      // (C:\...\bin\clang++.exe), so the old '/'-only split left dir="" -> llvm-strip/objdump/dwarfdump fell
      const cut = Math.max(clang.lastIndexOf("/"), clang.lastIndexOf("\\"));
      const dir = cut >= 0 ? clang.slice(0, cut + 1) : "";
      const exe = process.platform === "win32" ? ".exe" : "";
      const tool = (n: string) => dir + n + exe;   // llvm-{strip,objdump,dwarfdump} next to clang, else PATH
      const dbg = join(o.outDir, `${o.name}.debug.wasm`);
      await copyFile(wasm, dbg);              // keep the DWARF copy as the sidecar
      const lj = join(o.outDir, `${o.name}.lines.json`);
      if (writeLineMap(dbg, lj, { objdump: tool("llvm-objdump"), dwarfdump: tool("llvm-dwarfdump") })) linesJson = lj;
      if (Bun.spawnSync([tool("llvm-strip"), "--strip-debug", wasm]).exitCode === 0) debugWasm = dbg;   // deploy stripped
    } catch {}
  }
  return { ok: exitCode === 0, wasm, wrapper, stderr, exitCode, debugWasm, linesJson };
}
