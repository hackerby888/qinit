// Generates the browser snapshot committed with Qinit. Core-lite is a generation-time input only;
// browser consumers import the tracked module and never need a core checkout.
import { createHash } from "node:crypto";
import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { assembleQpiHeader, GENERATOR_VERSION } from "../src/qpi-snapshot";

const args = process.argv.slice(2);
const flag = (name: string): boolean => args.includes(name);
const opt = (name: string): string | undefined => {
  const i = args.indexOf(name);
  return i >= 0 ? args[i + 1] : undefined;
};

const pkgDir = resolve(import.meta.dir, "..");
const manifestPath = join(pkgDir, "core-snapshot.json");
const outputPath = join(pkgDir, "src", "generated", "qpi-snapshot.ts");
const quiet = flag("--quiet");
const log = (msg: string) => {
  if (!quiet) console.log(`[qpi-snapshot] ${msg}`);
};

const corePath = opt("--core") ?? process.env.QINIT_CORE;
if (!corePath) {
  console.error("[qpi-snapshot] no core checkout: pass --core <path> or set QINIT_CORE");
  process.exit(1);
}
const core = resolve(corePath);
if (!existsSync(join(core, "src", "contracts", "qpi.h"))) {
  console.error(`[qpi-snapshot] ${core} is not a core checkout (src/contracts/qpi.h missing)`);
  process.exit(1);
}

interface SnapshotManifest {
  core: { repository: string; commit: string };
  generatorVersion: number;
  snapshotHash: string;
}

const manifest = JSON.parse(readFileSync(manifestPath, "utf8")) as SnapshotManifest;

function render(): { hash: string; module: string } {
  const snapshot = assembleQpiHeader(core);
  const hash = "sha256:" + createHash("sha256").update(snapshot).digest("hex");
  const pkg = JSON.parse(readFileSync(join(pkgDir, "package.json"), "utf8"));
  const meta = {
    snapshotHash: hash,
    coreCommit: manifest.core.commit,
    generatorVersion: GENERATOR_VERSION,
    qinitCompileVersion: pkg.version as string,
  };
  return {
    hash,
    module:
      "// Generated by tools/gen-qpi-snapshot.ts — do not edit; commit updates with their core pin.\n" +
      `// core: ${meta.coreCommit} · generator v${GENERATOR_VERSION}\n` +
      `export const QPI_SNAPSHOT_META = ${JSON.stringify(meta, null, 2)} as const;\n\n` +
      `export const QPI_SNAPSHOT: string = ${JSON.stringify(snapshot)};\n`,
  };
}

function verify(hash: string): void {
  if (manifest.generatorVersion !== GENERATOR_VERSION) {
    throw new Error(`generator v${GENERATOR_VERSION} != manifest v${manifest.generatorVersion} — update core-snapshot.json`);
  }
  if (manifest.snapshotHash !== hash) {
    throw new Error(`snapshot hash mismatch\n  generated: ${hash}\n  manifest:  ${manifest.snapshotHash}\n  (core at ${core}, expected commit ${manifest.core.commit})`);
  }
}

try {
  const generated = render();
  if (flag("--verify") || flag("--check")) {
    verify(generated.hash);
    log(`verified against ${manifestPath}`);
  }
  if (flag("--check")) {
    if (!existsSync(outputPath) || readFileSync(outputPath, "utf8") !== generated.module) {
      throw new Error(`${outputPath} is stale; regenerate it from the pinned core checkout`);
    }
    log(`${outputPath} is current`);
  } else {
    const changed = !existsSync(outputPath) || readFileSync(outputPath, "utf8") !== generated.module;
    if (changed) {
      mkdirSync(dirname(outputPath), { recursive: true });
      writeFileSync(outputPath, generated.module);
    }
    log(changed ? `generated ${outputPath} (${generated.hash})` : `up to date (${generated.hash})`);
  }
} catch (error) {
  console.error(`[qpi-snapshot] ${(error as Error).message}`);
  process.exit(1);
}
