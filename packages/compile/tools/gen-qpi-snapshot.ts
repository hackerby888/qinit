// CLI for generating browser snapshot artifacts: `.generated/qpi-snapshot.{txt,ts,meta.json}`.
import { createHash } from "node:crypto";
import { execSync } from "node:child_process";
import { existsSync, mkdirSync, readFileSync, watch, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { assembleQpiHeader, snapshotInputFiles, GENERATOR_VERSION } from "../src/qpi-snapshot";

const args = process.argv.slice(2);
const flag = (name: string): boolean => args.includes(name);
const opt = (name: string): string | undefined => {
  const i = args.indexOf(name);
  return i >= 0 ? args[i + 1] : undefined;
};

const pkgDir = resolve(import.meta.dir, "..");
const outDir = join(pkgDir, ".generated");
const manifestPath = join(pkgDir, "core-snapshot.json");
const quiet = flag("--quiet");
const log = (msg: string) => {
  if (!quiet) console.log(`[qpi-snapshot] ${msg}`);
};

const corePath = opt("--core") ?? process.env.QINIT_CORE;
if (!corePath) {
  console.error("[qpi-snapshot] no core checkout: pass --core <path> or set QINIT_CORE");
  process.exit(1);
}
const core = resolve(corePath);
if (!existsSync(join(core, "src", "contracts", "qpi.h"))) {
  console.error(`[qpi-snapshot] ${core} is not a core checkout (src/contracts/qpi.h missing)`);
  process.exit(1);
}

function coreCommit(): string {
  try {
    const sha = execSync("git rev-parse HEAD", { cwd: core, stdio: ["ignore", "pipe", "ignore"] }).toString().trim();
    const dirty = execSync("git status --porcelain -- src", { cwd: core, stdio: ["ignore", "pipe", "ignore"] }).toString().trim();
    return dirty ? `${sha}-dirty` : sha;
  } catch {
    return "unknown";
  }
}

function generate(): { hash: string; wrote: boolean } {
  const snapshot = assembleQpiHeader(core);
  const hash = "sha256:" + createHash("sha256").update(snapshot).digest("hex");

  const metaPath = join(outDir, "qpi-snapshot.meta.json");
  if (existsSync(metaPath)) {
    try {
      const prev = JSON.parse(readFileSync(metaPath, "utf8"));
      if (prev.snapshotHash === hash && prev.generatorVersion === GENERATOR_VERSION) {
        return { hash, wrote: false };
      }
    } catch {
      // unreadable meta — regenerate
    }
  }

  const pkg = JSON.parse(readFileSync(join(pkgDir, "package.json"), "utf8"));
  const meta = {
    snapshotHash: hash,
    coreCommit: coreCommit(),
    generatorVersion: GENERATOR_VERSION,
    qinitCompileVersion: pkg.version as string,
  };

  mkdirSync(outDir, { recursive: true });
  writeFileSync(join(outDir, "qpi-snapshot.txt"), snapshot);
  writeFileSync(
    join(outDir, "qpi-snapshot.ts"),
    "// Generated by tools/gen-qpi-snapshot.ts — do not edit, do not commit.\n" +
      `// core: ${meta.coreCommit} · generator v${GENERATOR_VERSION}\n` +
      `export const QPI_SNAPSHOT_META = ${JSON.stringify(meta, null, 2)} as const;\n\n` +
      `export const QPI_SNAPSHOT: string = ${JSON.stringify(snapshot)};\n`,
  );
  writeFileSync(metaPath, JSON.stringify(meta, null, 2) + "\n");
  return { hash, wrote: true };
}

function verify(hash: string): void {
  if (!existsSync(manifestPath)) {
    console.error(`[qpi-snapshot] --verify: manifest ${manifestPath} not found`);
    process.exit(1);
  }
  const manifest = JSON.parse(readFileSync(manifestPath, "utf8"));
  if (manifest.generatorVersion !== GENERATOR_VERSION) {
    console.error(`[qpi-snapshot] --verify: generator v${GENERATOR_VERSION} != manifest v${manifest.generatorVersion} — update core-snapshot.json`);
    process.exit(1);
  }
  if (manifest.snapshotHash !== hash) {
    console.error(`[qpi-snapshot] --verify: snapshot hash mismatch\n  generated: ${hash}\n  manifest:  ${manifest.snapshotHash}\n  (core at ${core}, expected commit ${manifest.core.commit})`);
    process.exit(1);
  }
  log(`verified against ${manifestPath}`);
}

const first = generate();
log(first.wrote ? `generated ${join(outDir, "qpi-snapshot.ts")} (${first.hash})` : `up to date (${first.hash})`);
if (flag("--verify")) {
  verify(first.hash);
}

if (flag("--watch")) {
  log(`watching ${core}/src for header changes…`);
  let timer: ReturnType<typeof setTimeout> | null = null;
  const regen = () => {
    if (timer) clearTimeout(timer);
    timer = setTimeout(() => {
      try {
        const r = generate();
        if (r.wrote) log(`regenerated (${r.hash})`);
      } catch (e) {
        console.error(`[qpi-snapshot] regeneration failed: ${(e as Error).message}`);
      }
    }, 150);
  };
  const watched = new Set<string>();
  for (const f of snapshotInputFiles(core)) {
    const dir = dirname(f);
    if (watched.has(dir) || !existsSync(dir)) continue;
    watched.add(dir);
    watch(dir, regen);
  }
}
